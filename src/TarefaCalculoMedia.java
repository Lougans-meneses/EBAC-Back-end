public class TarefaCalculoMedia {
    public static void main(String[] args) throws Exception {
        int nota1 = 5;
        int nota2 = 8;
        int nota3 = 9;
        int nota4 = 10;

        int media = (nota1 + nota2 + nota3 + nota4) / 4;
        System.out.println("A média das notas são: " + media);
    }
}
